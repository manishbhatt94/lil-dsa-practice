package manish.dsa.arrays.twopointers.GFG_ThreeSumSmaller;

public class ThreeSumSmaller {

	private SolutionApproach approach;

	public ThreeSumSmaller(SolutionApproach approach) {
		super();
		this.approach = approach;
	}

	public long countTriplets(int n, int sum, long arr[]) {
		return approach.countTriplets(n, sum, arr);
	}

}
